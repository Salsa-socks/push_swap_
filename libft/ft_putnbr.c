#include "libft.h"

void	ft_putnbr(int i)
{
	if (i == -2147483648)
		ft_putstr("-2147483648");
	else if (i < 0)
	{
		ft_putchar('-');
		ft_putnbr(-i);
	}
	else if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + '0');
	}
	else
		ft_putchar(i + '0');
}
